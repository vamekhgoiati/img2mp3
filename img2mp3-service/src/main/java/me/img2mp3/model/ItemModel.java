package me.img2mp3.model;

/**
 * @author Vamekhi
 *         17-06-2018
 */
public class ItemModel {
    private String id;
    private String image;
    private String mp3;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getMp3() {
        return mp3;
    }

    public void setMp3(String mp3) {
        this.mp3 = mp3;
    }
}
